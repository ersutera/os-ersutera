volatile uint32 *test_dev = (uint32 *) VIRT_TEST;
uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  kexit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return kfork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return kwait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int t;
  int n;

  argint(0, &n);
  argint(1, &t);
  addr = myproc()->sz;

  if(t == SBRK_EAGER || n < 0) {
    if(growproc(n) < 0) {
      return -1;
    }
  } else {
    // Lazily allocate memory for this process: increase its memory
    // size but don't allocate memory. If the processes uses the
    // memory, vmfault() will allocate it.
    if(addr + n < addr)
      return -1;
    myproc()->sz += n;
  }
  return addr;
}

uint64
sys_pause(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  if(n < 0)
    n = 0;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kkill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

uint64
sys_shutdown(void)
{
  *test_dev = 0x5555;  // MMIO write: shut down
  return 0;
}

uint64
sys_reboot(void)
{
  *test_dev = 0x7777;  // MMIO write: reboot
  return 0;
}

uint64
sys_rtcgettime(void)
{
    volatile uint32 *rtc = (uint32 *) VIRT_RTC;

    uint32 lo = rtc[0];     // low 32 bits
    uint32 hi = rtc[1];     // high 32 bits

    return ((uint64)hi << 32) | lo;
}

uint64
sys_strace_on(void)
{
  struct proc *p = myproc();
  if (!p) return -1;
  p->tracing = 1;
  return 0;
}
